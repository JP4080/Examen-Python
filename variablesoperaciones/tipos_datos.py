# Diferentes Datos
entero = 42         # Entero
decimal = 3.14      # Decimal
texto = "Python"    # Texto
booleano = True     # Verdadero o falso
lista = [1, 2, 3]   # Lista


# Su Tipo 
print(entero, type(entero))     # int   -  42
print(decimal, type(decimal))   # float -  3.14
print(texto, type(texto))       # str   -  Python
print(booleano, type(booleano)) # bool  -  True
print(lista, type(lista))       # list  -  [1, 2, 3]


